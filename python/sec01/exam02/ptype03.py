
mystring="hello world!"

print( mystring[0:5])
print( mystring[0:-6]) #문자열의 첫 번째 문자부터 시작해서 문자열의 끝에서 6번째 문자 바로 앞까지 표시
print( mystring[0:-5]) #인덱스 0부터 시작하여 끝에서 5번째 문자를 포함하지 않는 부분 즉 바로 앞까지 표시
print( mystring[0:-7]) #공백이 포함되지 않으며 공백문자 바로 앞에서 탐색이 끝남
print( mystring[::])
print( mystring[:])
print( mystring[:-1])
print( mystring[-1:])

